# 2) FUNZIONE PLOT: tutti i metodi per un pezzo (seed dots + mean line)
# ============================================================
def plot_piece_all_methods_with_seed_dots_and_mean(
    piece_name: str,
    good_fractions: np.ndarray,
    results_by_method: dict,
    title_metric: str = "Pixel-level AUC-PRO",
    fixed_ylim: tuple | None = None,
    seed_dot_size: int = 18,
    seed_dot_alpha: float = 0.25,
    mean_marker_size: int = 6,
    mean_linewidth: float = 2.2,
):
    """
    Grafico unico per un pezzo:
    - per ogni metodo: pallini seed (solo pallini) + linea media con marker
    """

    # Colori fissi e leggibili (metodo -> colore)
    method_colors = {
        "SPADE": "red",
        "PaDiM": "blue",
        "InReaCh": "green",
        "FAPM": "purple",
    }

    plt.figure(figsize=(12, 6))

    for method, pieces_dict in results_by_method.items():
        if piece_name not in pieces_dict:
            continue

        seed_lists = pieces_dict[piece_name]  # lista di seed, ognuno  una lista lunga len(good_fractions)

        # --- controllo forma dati ---
        # seed_lists: [ [20], [20], ... ]
        seed_arr = np.array(seed_lists, dtype=float)  # shape: (n_seeds, n_gf)
        if seed_arr.ndim != 2 or seed_arr.shape[1] != len(good_fractions):
            raise ValueError(
                f"{method} {piece_name}: forma sbagliata. "
                f"Atteso (n_seeds, {len(good_fractions)}), ottenuto {seed_arr.shape}."
            )

        color = method_colors.get(method, None)

        # --- pallini seed: SOLO pallini (niente linee) ---
        # faccio uno scatter per ogni seed (stesso colore del metodo)
        for s in range(seed_arr.shape[0]):
            plt.scatter(
                good_fractions,
                seed_arr[s],
                s=seed_dot_size,
                alpha=seed_dot_alpha,
                color=color,
                edgecolors="none",
                zorder=2
            )

        # --- media: linea + marker ---
        mean_curve = seed_arr.mean(axis=0)
        plt.plot(
            good_fractions,
            mean_curve,
            marker="o",
            markersize=mean_marker_size,
            linewidth=mean_linewidth,
            color=color,
            label=method,
            zorder=3
        )

    plt.title(f"{piece_name}  {title_metric} vs Good Fraction")
    plt.xlabel("Good Fraction")
    plt.ylabel(title_metric)
    plt.grid(True, alpha=0.35)
    plt.legend(loc="center left")

    # stessa scala Y per confrontare i pezzi
    if fixed_ylim is not None:
        plt.ylim(fixed_ylim)

    plt.tight_layout()
    plt.show()


